public class Calculadora {

        public int soma(int numSoma1, int numSoma2) {
            return numSoma1 + numSoma2;
        }

        public int subtracao(int numSubtracao1, int numSubtracao2) {
            return numSubtracao1 - numSubtracao2;
        }

        public int multiplicacao(int numMultipicacao1, int numMultiplicacao2) {
            return numMultipicacao1 * numMultiplicacao2;
        }

        public int divisao(int numDivisao1, int numDivisao2) {
            return numDivisao1 / numDivisao2;
        }
}
